def draw_row(row)
	row.each do |el|
		draw_leaves(el[1]) if el[0] == '#'
		draw_spaces(el[1]) if el[0] == ' '
		draw_ornaments(el[1]) if el[0] == '*'
		draw_branches(el[1]) if el[0] == '|'
	end
	print "\n"
end

def draw_leaves(n)
	data = []
	n.times { |_| data << '#' }
	print "\033[0;32m#{data.join('')}\033[0m"
end

def draw_spaces(n)
	data = []
	n.times { |_| data << ' ' }
	print data.join('')
end

def draw_ornaments(n)
